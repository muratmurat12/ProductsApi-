package ca.vanier;

public class Object_Manager {

    private int num;
    private String products;

    private String name;

    public String getName() {
        return name;
    }

    public void setName(String N) {
        this.name = N;
    }

    public String getproducts() {
        return products;
    }

    public void setproducts(String A) {
        this.products = A;
    }

    public int getnum() {
        return num;
    }

    public void setnum(int n) {
        this.num = n;
    }

}
